#Assignment Operators
#1
x = 5  # переводим в двоичную форму 

x &= 3

print(x) #Output:2(переводим в десятичную форму), multiplication by bytes 

#2
x = 5 

x |= 3

print(x) #Output:7(переводим в десятичную форму), sum by bytes

#3
x = 5

x ^= 3

print(x) #Output:6, XOR operation by bytes

#4
print(x := 3) #Output: 3(присваивает переменной x значение 3, а затем  выводит значение переменной x, которое равно 3.)







